import math

alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
decision = True
def caesar(start_text, shift_amount, cipher_direction):
    new_text = ""
    for i in start_text:
        if i in alphabet:
            x = int(alphabet.index(i))
            if cipher_direction == "encode":
                new_position = x + shift_amount
                new_num = new_position - 26 * math.floor(new_position / 26)
            elif cipher_direction == "decode":
                new_position = x - shift_amount
                new_num = new_position + -26 * math.floor(new_position / 26)
            new_text += alphabet[new_num]
        else:
            new_text += i
    print(f"The {cipher_direction}d text is {new_text}")

while decision:
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    caesar(start_text=text, shift_amount=shift, cipher_direction=direction)
    willing = input("Type 'yes' if you want to go again. Otherwise type 'no'\n")
    if willing == "no":
        decision = False
        print("Goodbye")